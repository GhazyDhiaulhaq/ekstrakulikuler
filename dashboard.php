    document.addEventListener("DOMContentLoaded", function () {
        const animatedElements = document.querySelectorAll('.animate__animated');
        const animationClass = 'animate__fadeInUp'; // Nama animasi yang digunakan
        const animationDuration = 1000; // Durasi animasi dalam milidetik (1 detik sesuai Animate.css)

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    // Tambahkan kelas animasi saat elemen masuk ke dalam tampilan
                    entry.target.classList.add(animationClass);
                } else {
                    // Hapus kelas animasi setelah durasi animasi selesai saat elemen keluar dari tampilan
                    setTimeout(() => {
                        entry.target.classList.remove(animationClass);
                    }, animationDuration);
                }
            });
        }, { threshold: 0.01 }); // Animasi dimulai saat 10% elemen terlihat

        animatedElements.forEach(element => {
            observer.observe(element);
        });
    });
</script>



